# Function to return a value of a position
# Position - tuple (x, y)
# Piece type - two char representing color + type
def get_value_of_position(pos, type):
    x, y = pos

    # Switch up down if black
    if type[0] == 'b':
        y = 7 - y

    index_in_table = x + (y * 8)
    return PIECE_SQUARE_TABLE[type[1]][index_in_table]
